package org.uploads.entity;

public class Lot {
    String idLot;
    String nom;
    String date;
    public String getIdLot() {
        return idLot;
    }
    public void setIdLot(String idLot) {
        this.idLot = idLot;
    }
    public String getNom() {
        return nom;
    }
    public void setNom(String nom) {
        this.nom = nom;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
    
}
